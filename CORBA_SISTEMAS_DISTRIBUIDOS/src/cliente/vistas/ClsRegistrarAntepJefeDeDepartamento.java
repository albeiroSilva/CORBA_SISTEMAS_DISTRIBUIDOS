    public void llenarComboBox() throws RemoteException{
        
        usuarioDTO[] usuarios = objRemoto.listarUsuarios();
        
        for (int i = 0; i < usuarios.length; i++) {
            if (usuarios[i].getTipoUsuario().equals("1")) {
                
                jComboBox2.addItem(usuarios[i].getNombreApellidos());
                jComboBox3.addItem(usuarios[i].getNombreApellidos());
                
            }
            else if (usuarios[i].getTipoUsuario().equals("2")) {
                
                jComboBox4.addItem(usuarios[i].getNombreApellidos());
                jComboBox5.addItem(usuarios[i].getNombreApellidos());
            }
            
        }
    }
