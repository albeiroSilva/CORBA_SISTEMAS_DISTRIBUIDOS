    public void obtenerDatos() throws RemoteException {

        String titulo = jTextField1.getText();
        String modalidad = (String) jComboBox1.getSelectedItem();
        String estudiante_1 = (String)jComboBox2.getSelectedItem();
        String estudiante_2 = (String)jComboBox3.getSelectedItem();
        String director = (String)jComboBox4.getSelectedItem();
        String co_director = (String)jComboBox5.getSelectedItem();
        
        Date  fecha=jDateChooser1.getDate();
        DateFormat f=new SimpleDateFormat("dd-MM-yyyy");
        String fechaR1=f.format(fecha);

        

        if (jTextField1.getText().equals("") ) {
            JOptionPane.showMessageDialog(null, "Uno o varios campos están vacíos");
        } else {
            anteproyectoDTO objAnteproyecto = new anteproyectoDTO(0, titulo, modalidad, estudiante_1, estudiante_2, director, co_director, fechaR1, fechaR1, 2, 1, 0);

            if (objRemoto.registrarAnteproyecto(objAnteproyecto)) {
                JOptionPane.showMessageDialog(null, "registro realizado con éxito");
            } else {
                JOptionPane.showMessageDialog(null, "Algo falló");
            }
        }

        impiarCampos();

    }
